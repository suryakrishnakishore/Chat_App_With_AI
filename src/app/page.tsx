"use client";

import LeftPanel from "@/components/home/left-panel";
import RightPanel from "@/components/home/right-panel";
import Auth from "@/components/auth/page";
import useStore from "@/store";
import { useEffect } from "react";
import { getSocket, initSocket } from "@/lib/socket";
import { registerMessageEvents, registerPresenceEvents } from "@/lib/useSocket";
import ClientOnly from "@/components/client-only";

export default function Home() {
  const { user, signOut, hydrated } = useStore((state) => state);
  console.log("Page User: ", user);
  useEffect(() => {
    if (!hydrated) return;
    if (!user) return;
    console.log("Token for socket: ", user.token);

    initSocket(user.token);
    registerMessageEvents();
    registerPresenceEvents();
    const socket = getSocket();
    if (socket) {
      socket.emit("user:online", user._id);
      
    }
  }, [user]);
  if (!hydrated) {
    return null; // or loading spinner
  }

  if (!user) {
    return <Auth />;
  }

  return (
    <ClientOnly>
      <main className="m-5">
        <div className="flex overflow-y-hidden h-[calc(100vh-40px)] max-w-[1700px] mx-auto bg-[hsl(var(--left-panel))]">
          <div className="fixed top-0 left-0 w-full h-36 bg-[hsl(var(--green-primary))] dark:bg-transparent -z-30"></div>
          <LeftPanel />
          <RightPanel />
        </div>
      </main>
    </ClientOnly>
  )
}